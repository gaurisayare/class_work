//program to Swap two numbers using third variable
import java.util.Scanner;
class Swap
{
    public static void main(String args[])
	{
	  int first_value, sec_value, temp;  //first_value and sec_value are to be swap
      
	  Scanner sc = new Scanner(System.in);
	  System.out.println("Enter the first value and second value");
	  first_value = sc.nextInt();
	  sec_value = sc.nextInt();
	  
	  System.out.println("Before Swapping the values are: x="+first_value+" y="+sec_value);
	 
	  temp = first_value;  //Swapping
	  first_value = sec_value;
	  sec_value = temp;
	  
	  System.out.println("After Swapping the values are: x="+first_value+" y="+sec_value);
	  sc.close();
	}
}

























































































































































































































































