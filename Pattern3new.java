class Pattern3new
{
	public static void main(String args[])
	{
		int i, j;                   //i is taken to iterate over row and j is for column
		for(i=1;i>=1;i--)
		{
			for(j=i;j<=5;j++)
			{
				System.out.print("*");         //dont write println here because if u write it u will get output in a single coloum.
			}
		System.out.println();
	    }
	}
}